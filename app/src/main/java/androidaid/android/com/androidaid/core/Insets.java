package androidaid.android.com.androidaid.core;

public class Insets {
    private int xInset;
    private int yInset;

    public Insets(int xInset, int yInset) {
        this.xInset = xInset;
        this.yInset = yInset;
    }

    public int getxInset() {
        return xInset;
    }

    public int getyInset() {
        return yInset;
    }
}
